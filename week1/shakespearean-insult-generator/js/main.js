// This function takes a maximum integer value, and returns
// a random integer between 0 and max - 1 (it's -1 because of the .floor())
const generateRandomInt = function( max ){
  const random = Math.random() * max;
  return Math.floor( random );
};

// This function takes an array as its argument, and returns
// a randomly-chosen element from that array
const getRandomElementFromArray = function( array ){
  const randomIndex = generateRandomInt( array.length );
  // console.log('*** in getRandomElementFromArray(): array length is: ', array.length);
  // console.log('    ----- value to return: ', array[ randomIndex ]);
  return array[ randomIndex ];
  // return array[ generateRandomInt(array.length) ];
};

